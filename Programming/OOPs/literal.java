public class literal {
    public static void main(String[] args) {
        byte age = 34;
        System.out.println(age);

        char var1 = 'A';
        System.out.println(var1);

        float f1 = 4.7f;
        System.out.println(f1);

        int num1 = 67;
        System.out.println(num1);

        double d1 = 9.00d;
        System.out.println(d1);
    }
}